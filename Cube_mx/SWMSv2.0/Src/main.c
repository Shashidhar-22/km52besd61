		HAL_I2C_Mem_Write(&hi2c1,0x50<<1,0x00,0x01,(unsigned char *)eeprom_buff,10,100);
		HAL_I2C_Mem_Read(&hi2c1,0x50<<1,0x00,0x01,(unsigned char *)eeprom_buff,10,100);
